package com.company;

public class Arithmetics {
    public static double calculateVolume(double num1) { return num1*num1*num1; };
    public static double calculatePerimeter(double num1) {return num1*12;};

    public static double inchToCm(double num1, double num2) {return (num1*12 + num2) * 2.54; }
}
